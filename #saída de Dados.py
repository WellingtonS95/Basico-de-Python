#Usaremos a função 'int' que converte uma string em um numero inteiro.
# 'input' que do inglês literalmente é "entrada". Auxilia o usuário imprimindo um dado na tela informando que tipo de dado o programa está aguardando receber.
#Usaremos uma variável qualquer, exemplo 'n', para numeros.

n = int(input('Digite um número:'))
n2 = int(input('Digite outro número:'))
